# 重複時は最高スコアを採用するため、既存値を上書きしない（高い方のみ）
def get_first_station_score(station_name: str) -> float:
    """駅名から始発駅スコアを返す。未登録は0。"""
    s = (station_name or "").strip().rstrip("駅")
    if s in FIRST_STATION_SCORES:
        return FIRST_STATION_SCORES[s]
    return FIRST_STATION_SCORES.get(station_name, 0.0)
